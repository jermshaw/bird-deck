// Helper function to convert color names to hex codes
const colorMap: Record<string, string> = {
  "Brown": "#8B4513",
  "Gray": "#808080", 
  "Grey": "#808080",
  "Black": "#000000",
  "Red": "#FF0000",
  "Yellow": "#FFD700",
  "Orange": "#FFA500",
  "Blue": "#0000FF",
  "Green": "#008000",
  "White": "#FFFFFF",
  "Pink": "#FFC0CB",
  "Purple": "#800080",
  "Violet": "#8A2BE2"
};

// Helper function to parse colors from CSV format
function parseColors(colorString: string): string[] {
  if (!colorString) return ["#808080"];
  
  return colorString.split(",")
    .map(color => color.trim())
    .map(color => colorMap[color] || "#808080");
}

